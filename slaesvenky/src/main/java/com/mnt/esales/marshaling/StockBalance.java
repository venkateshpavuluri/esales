public class StockBalance {
	private String stockBalId;
	private String prodId;
	private String balanceStock;
	private String newStock;
	private String stockEntry;
	private String stockId;
	/**
	 * @return the stockBalId
	 */
	public String getStockBalId() {
		return stockBalId;
	}
	/**
	 * @param stockBalId the stockBalId to set
	 */
	public void setStockBalId(String stockBalId) {
		this.stockBalId = stockBalId;
	}
	/**
	 * @return the prodId
	 */
	public String getProdId() {
		return prodId;
	}
	/**
	 * @param prodId the prodId to set
	 */
	public void setProdId(String prodId) {
		this.prodId = prodId;
	}
	/**
	 * @return the balanceStock
	 */
	public String getBalanceStock() {
		return balanceStock;
	}
	/**
	 * @param balanceStock the balanceStock to set
	 */
	public void setBalanceStock(String balanceStock) {
		this.balanceStock = balanceStock;
	}
	/**
	 * @return the newStock
	 */
	public String getNewStock() {
		return newStock;
	}
	/**
	 * @param newStock the newStock to set
	 */
	public void setNewStock(String newStock) {
		this.newStock = newStock;
	}
	/**
	 * @return the stockEntry
	 */
	public String getStockEntry() {
		return stockEntry;
	}
	/**
	 * @param stockEntry the stockEntry to set
	 */
	public void setStockEntry(String stockEntry) {
		this.stockEntry = stockEntry;
	}
	/**
	 * @return the stockId
	 */
	public String getStockId() {
		return stockId;
	}
	/**
	 * @param stockId the stockId to set
	 */
	public void setStockId(String stockId) {
		this.stockId = stockId;
	}
	

}
