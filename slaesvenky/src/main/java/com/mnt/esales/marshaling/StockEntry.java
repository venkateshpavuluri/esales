package com.mnt.esales.marshaling;

/**
 * @author venkateshp
 *
 */
public class StockEntry {
private String stockId;
private String cost;
private String discount;
private String mrp;
private String productId;
private String quantity;
private String stockEntry;
/**
 * @return the stockId
 */
public String getStockId() {
	return stockId;
}
/**
 * @param stockId the stockId to set
 */
public void setStockId(String stockId) {
	this.stockId = stockId;
}
/**
 * @return the cost
 */
public String getCost() {
	return cost;
}
/**
 * @param cost the cost to set
 */
public void setCost(String cost) {
	this.cost = cost;
}
/**
 * @return the discount
 */
public String getDiscount() {
	return discount;
}
/**
 * @param discount the discount to set
 */
public void setDiscount(String discount) {
	this.discount = discount;
}
/**
 * @return the mrp
 */
public String getMrp() {
	return mrp;
}
/**
 * @param mrp the mrp to set
 */
public void setMrp(String mrp) {
	this.mrp = mrp;
}
/**
 * @return the productId
 */
public String getProductId() {
	return productId;
}
/**
 * @param productId the productId to set
 */
public void setProductId(String productId) {
	this.productId = productId;
}
/**
 * @return the quantity
 */
public String getQuantity() {
	return quantity;
}
/**
 * @param quantity the quantity to set
 */
public void setQuantity(String quantity) {
	this.quantity = quantity;
}
/**
 * @return the stockEntry
 */
public String getStockEntry() {
	return stockEntry;
}
/**
 * @param stockEntry the stockEntry to set
 */
public void setStockEntry(String stockEntry) {
	this.stockEntry = stockEntry;
}


}
