public class ExpensesInfo {
	private String expenseinfoId;
	private String expensesId;
	private String expensesDate;
	private String expensesTotal;
	private String status;
	/**
	 * @return the expenseinfoId
	 */
	public String getExpenseinfoId() {
		return expenseinfoId;
	}
	/**
	 * @param expenseinfoId the expenseinfoId to set
	 */
	public void setExpenseinfoId(String expenseinfoId) {
		this.expenseinfoId = expenseinfoId;
	}
	/**
	 * @return the expensesId
	 */
	public String getExpensesId() {
		return expensesId;
	}
	/**
	 * @param expensesId the expensesId to set
	 */
	public void setExpensesId(String expensesId) {
		this.expensesId = expensesId;
	}
	/**
	 * @return the expensesDate
	 */
	public String getExpensesDate() {
		return expensesDate;
	}
	/**
	 * @param expensesDate the expensesDate to set
	 */
	public void setExpensesDate(String expensesDate) {
		this.expensesDate = expensesDate;
	}
	/**
	 * @return the expensesTotal
	 */
	public String getExpensesTotal() {
		return expensesTotal;
	}
	/**
	 * @param expensesTotal the expensesTotal to set
	 */
	public void setExpensesTotal(String expensesTotal) {
		this.expensesTotal = expensesTotal;
	}
	/**
	 * @return the status
	 */
	public String getStatus() {
		return status;
	}
	/**
	 * @param status the status to set
	 */
	public void setStatus(String status) {
		this.status = status;
	}
	

}
